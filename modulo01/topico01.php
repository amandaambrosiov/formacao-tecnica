                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            A intersetorialidade na execução do atendimento socioeducativo, prescinde da compreensão que não é o cometimento de um ato infracional que pode atribuir ao/à adolescente ou jovem a pecha de uma determinação irremediavelmente periculosa, mas, ao contrário, deve afiançar a leitura das circunstâncias de vulnerabilidade e risco social que podem estar associadas a esse ato cometido. Qual a relação desse(a) jovem com a escola? Que oportunidades de acesso à arte, cultura, esporte e lazer lhe foram proporcionadas? Como se opera sua rede de apoio e qual a condição socioeconômica de sua família, seja essa consanguínea ou afetiva? A execução de uma medida socioeducativa não retira desse público sua condição de sujeitos de direitos e, portanto, não os torna clientes específicos de uma política pública. Não deixam de ser munícipes e, como tais, beneficiários(as) das políticas sociais?
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            Desta forma, a socioeducação, entendida como processo de formação humana integral, se apresenta como uma política que prima pela educação social dos sujeitos e tem como expectativa a reprovação da conduta ilícita, conforme prevê o SINASE, mas aliada a ações que devem ter em foco o caráter pedagógico e o reconhecimento de que o adolescente é capaz de ter uma mudança de conduta. A educação social, por definição, é aquela que favorece o desenvolvimento da sociabilidade do sujeito, promovendo a sua autonomia e a sua participação crítica e construtiva no seu processo formativo. A <a class="link_tooltip" data-bs-custom-class="custom-tooltip" data-bs-toggle="tooltip" data-bs-title="Costa ACG. Socioeducação. Secretaria Especial dos Direitos Humanos, Brasília, 2006">
                                socioeducação
                            </a> propõe um trabalho social reflexivo por meio de procedimentos educativos voltados para a modificação das contingências que limitam a integração social, e para a geração de novas formas de interações intra e interpessoais e, como consequência, uma melhor qualidade de vida.
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            A questão da intersetorialidade evoca ainda, no caso da socioeducação, o ensejo do princípio da incompletude institucional do sistema, referenciado aqui pelo professor Antônio Carlos Gomes da Costa e seu viés de uma “articulação orgânica e permanente” para execução da política socioeducativa, com a visão de uma integralidade ainda aquém do indicado.
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            Há cerca de 5 anos, resultado de uma pesquisa de campo desenvolvida na Assembleia Legislativa, em parceria com o Governo do Estado, Fundo das Nações Unidas para a Infância (Unicef) e sociedade civil, foi lançado o relatório final <a class="link_tooltip" data-bs-custom-class="custom-tooltip" data-bs-toggle="tooltip" data-bs-title="As publicações produzidas como resultado das pesquisas e outras informações associadas estão disponíveis no portal Cada Vida Importa: https://cadavidaimporta.com.br/">
                                "Cada Vida Importa”
                            </a>, que buscou investigar a trajetória de vida de adolescentes assassinados em sete cidades cearenses: Fortaleza, Juazeiro do Norte, Sobral, Maracanaú, Caucaia, Horizonte e Eusébio. Diante dos resultados encontrados, foram apresentadas evidências de risco e recomendações à rede de proteção, reivindicando a necessidade de um sistema integrado, a fim de garantir a proteção integral.
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            A estruturação do trabalho permite um direcionamento assertivo de conhecimentos, saberes e de afinidades, buscando estabelecer discussões parametrizadas. É um momento para definição de objetivos relevantes e alcançáveis, formulação de estratégias e prioridades. O processo deve ser de escuta, de discussão, de participação. Os espaços de diálogo precisam ser acessíveis e a comunicação necessita ser fluída, de forma que haja compreensão clara de todo o necessário.
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            Por fim, considerando a elaboração do plano com a sistematização dos elementos debatidos, faz-se necessário a submissão à aprovação e garantia de divulgação e publicidade do conteúdo.
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            Caracterizando um bom planejamento e com temporalidade demarcada, o acompanhamento ao plano decenal precisa ser também encarado como uma responsabilidade contínua, com mecanismos de avaliação baseados em instrumentos de mensuração adequados que permitam a verificação da já mencionada efetividade das ações. Um bom monitoramento consegue identificar pontos que, necessitando de revisão, possibilitem adaptações ao longo do tempo.
                        </p>

