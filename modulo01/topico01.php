                        <h3 class="Subtopico">Uma breve contextualização do direito da criança e do adolescente na perspectiva da doutrina da proteção integral</h3>

                        <p>
                            Com a adesão do Brasil a Convenção dos Direitos da Criança, através do Decreto nº 99.710/1990, ocorreu uma mudança de paradigma no qual a Doutrina da Proteção Integral vem sobrepor-se a Doutrina da Situação Irregular, vigente até a promulgação da nossa Constituição Federal, que essencialmente segregava e discriminava crianças e adolescentes que eram considerados meros objetos de intervenção do Estado pela condição de abandono dos pais ou responsáveis; ou por incapacidade de manutenção financeira; ou por alguma inadaptação; ou pelo cometimento de infração penal, prevendo naquela época uma política de atendimento assistencialista e paternalista.
                        </p>

                        <div class="content_box_padrao">
                            <p>
                                A Doutrina da Proteção Integral serviu de base para a elaboração da legislação especial voltada ao segmento através da Lei nº 8.069/1990 (Estatuto da Criança e do Adolescente – ECA) que reconhece a criança e o adolescente como sujeitos de direitos, em condição peculiar de desenvolvimento e detentores de prioridade absoluta.
                            </p>
                        </div>

                        <p>
                            Desse modo, a Doutrina da Proteção Integral é um princípio fundamental que norteia a proteção dos direitos da criança e do adolescente, tendo como base a ideia de que crianças e adolescentes são sujeitos de direitos e merecem uma proteção especial, integral e prioritária por parte da família, da sociedade e do Estado. Essa abordagem reconhece que os jovens estão em fase de desenvolvimento e vulnerabilidade, sendo necessária uma atenção especial para garantir seu pleno desenvolvimento físico, mental, moral, espiritual e social.
                        </p>

                        <p>
                            Em relação ao adolescente autor de ato infracional, a Doutrina da Proteção Integral rompe com a Doutrina da Situação Irregular e estabelece a responsabilização e a integração social para os adolescentes que cometeram atos infracionais, assegurando-lhes direitos e garantias processuais. Em suma, a Doutrina da Proteção Integral aplicada ao adolescente em cumprimento de medida socioeducativa reconhece sua condição de sujeito de direitos e busca oferecer uma abordagem integrada e abrangente, visando sua reintegração social e seu desenvolvimento pleno, por meio do acesso a serviços, apoio familiar e comunitário, educação e demais direitos essenciais.
                        </p>

