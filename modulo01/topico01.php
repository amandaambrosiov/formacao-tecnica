                        <h3 class="Subtopico" id="item3">A comissão intersetorial e o plano decenal de atendimento socioeducativo</h3>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            O verbete planejamento é descrito em dicionário como “organização de uma tarefa com utilização de métodos apropriados” ou “determinação de ações para atingir metas estipuladas por uma empresa, órgão do governo etc”. Fora do dicionário, o ato de planejar faz parte de nossas atividades cotidianas, se encontra na lista do supermercado, na planilha financeira de gastos, na definição do roteiro de viagem. Nos ambientes profissionais, o planejamento está associado com elementos organizacionais típicos, como missão, visão, valores, mas também com conceitos extremamente relacionados à questão do desempenho em si, como eficácia, eficiência e efetividade, além de aspectos éticos como transparência, participação e sustentabilidade.
                        </p>

