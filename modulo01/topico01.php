                        <h3 class="Subtopico" id="item1">O SINASE</h3>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            O Sistema Nacional de Atendimento Socioeducativo (SINASE), instituído pela Lei nº 12.594/2012, é uma política pública do Brasil que tem como objetivo principal, regulamentar e promover a execução de medidas socioeducativas para os adolescentes autores de atos infracionais. Vem estabelecer diretrizes e princípios para a garantia dos direitos humanos e sociais desse público, bem como sua ressocialização e reintegração à sociedade, suprindo lacunas deixadas pelo Estatuto da Criança e do Adolescente (ECA).
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            O contexto de criação do SINASE partiu de um cenário do sistema socioeducativo no Brasil marcado por práticas punitivas, violências e negligências. Os adolescentes autores de atos infracionais eram tratados de forma desumana e suas condições de vida eram precárias. Em 1998, iniciou-se um debate sobre a proposta de uma lei para a execução das medidas socioeducativas, que só se concretizou com a sua sanção em janeiro de 2012.
                        </p>

                        <!-- CAIXA DE TEXTO IMPORTANTE -->
                        <div class="destaques wow fadeIn" data-wow-delay="0.3s">
                            <div class="caixaprincipal" id="titulo_importante">
                                <i class="fa-solid fa-triangle-exclamation"></i>
                                IMPORTANTE
                            </div>
                            <div class="content_box" id="conteudo_importante">
                                <p>O SINASE surge como uma resposta para limitar a discricionariedade e assegurar a natureza pedagógica das medidas socioeducativas, bem como uma abordagem mais humanizada e apropriada no atendimento socioeducativo. Sua contextualização histórica remete a um longo processo de modificações nas políticas direcionadas para adolescentes autores de atos infracionais no nosso país.</p>
                            </div>
                        </div>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            Durante muitos anos, o sistema socioeducativo no Brasil foi marcado por práticas repressivas. As políticas adotadas pautavam-se em um modelo puramente punitivista, onde o encarceramento e a segregação eram vistos como as soluções mais eficazes para a criminalidade juvenil.
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            As iniciativas para mudanças no sistema socioeducativo, ocorreram a partir da década de 90, por meio das organizações da sociedade civil, movimentos sociais e estudiosos da temática, que passaram a contestar a eficácia e a aplicação desse modelo vigente, tendo em vista que o ECA não fornecia uma estrutura abrangente para o atendimento socioeducativo.
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            Essa discussão propiciou a construção de uma nova perspectiva, pautada nos princípios dos direitos humanos e na proteção integral dos adolescentes. Esse novo modelo com finalidade pedagógica, foi ratificado pela Convenção das Nações Unidas sobre os Direitos da Criança.
                        </p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            Com isso, o caráter punitivo e sancionatório deu lugar à responsabilização do adolescente, com foco na reparação das consequências lesivas do ato infracional, priorizando a aplicação da Justiça Restaurativa e das práticas restaurativas.
                        </p>

                        <div class="content_box_padrao">
                            <p>
                                No paradigma de atendimento aos adolescentes autores de atos infracionais no Brasil, o SINASE os reconhece como sujeitos de direitos e, portanto, deve ser garantido o atendimento que respeite a dignidade e proporcione sua reintegração social.
                            </p>
                        </div>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            Ao longo desses anos, o SINASE tem passado por ajustes e aperfeiçoamentos, a fim de aprimorar o atendimento socioeducativo, e consequentemente a garantia dos direitos dos adolescentes autores de atos infracionais. A sua plena implementação ainda encontra inúmeros desafios, entretanto, a existência desse sistema representa um avanço significativo na busca por uma abordagem mais humanizada e efetiva para lidar com a questão da criminalidade juvenil no Brasil.
                        </p>

