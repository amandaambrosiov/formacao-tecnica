                        <h4 class="subsubtopico">Termo de Parceria/Cooperação Técnica</h4>

                        <p class="wow fadeIn" data-wow-delay="0.3s">
                            É o instrumento onde se pactua o recebimento e o acompanhamento dos adolescentes para a Prestação de Serviço à Comunidade e os objetivos dos pactuantes de recíproca cooperação, com um fim de interesse comum, de atuação conjunta na execução da Medida Socioeducativa de Prestação de Serviço à Comunidade – PSC, com base na Lei nº 8.069/1990, Resolução CNAS nº 109/2009 e Lei nº 12.594/2012.
                        </p>

