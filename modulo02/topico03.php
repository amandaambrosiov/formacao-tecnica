                        <p><strong>RELATÓRIO DE ACOMPANHAMENTO E AVALIAÇÃO:</strong></p>

                        <p>Deverão ser elaborados, periodicamente, pelo técnico de referência do serviço de MSE e encaminhados ao Poder Judiciário.</p>

                        <p>Versarão sobre a efetivação das metas e objetivos estabelecidos no PIA, servindo como parâmetro para avaliação do cumprimento da MSE com vistas a subsidiar a decisão judicial acerca da continuidade, da substituição ou da extinção da MSE aplicada.</p>

                        <p>O intervalo de tempo desses relatórios serão estabelecidos pela interlocução com o Poder Judiciário. Embora a decisão sobre a continuidade, a revisão ou extinção da MSE não seja de competência do técnico de referência, os relatórios de acompanhamento deverão conter informações que subsidiarão a decisão judicial do adolescente.</p>

