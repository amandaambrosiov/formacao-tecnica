                        <p><i class="seta bi bi-arrow-right-short"></i> Informações sobre atividades de cultura, esporte, lazer (o que gosta de fazer, se tem algum hobby, o que faz nas horas vagas, se já fez algum tipo de curso ou oficina para o desenvolvimento da expressão ou da criatividade, se existem grupos/equipamentos culturais em seu bairro);</p>

