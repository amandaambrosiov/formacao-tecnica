                        <h3 class="Subtopico" id="item3">Quem elabora o PIA?</h3>

                        <div class="content_box_padrao">
                            <p>
                                O PIA será elaborado sob a responsabilidade da equipe técnica do respectivo programa de atendimento, com a participação efetiva do adolescente e de sua família, representada por seus pais ou responsável. (Lei nº 12.594/2012, Art. 53º)
                            </p>
                        </div>

                        <p>As normativas vedam que o adolescente seja atendido por apenas um profissional. A Resolução CONANDA nº 119/2006 e a Norma Operacional Básica - Recursos Humanos (NOB-RH) do SUAS definem que o atendimento técnico deve ser interdisciplinar.</p>

                        <p>Nesse sentido, a construção do PIA precisa de um olhar múltiplo e articulado, contando com, no mínimo, a experiência do Serviço Social, Pedagogia e Psicologia, podendo ainda ter a colaboração de outros profissionais. Dispõe ainda sobre os princípios e diretrizes da gestão do trabalho no SUAS, indicando as equipes de referência dos equipamentos e serviços da Política de Assistência Social.</p>

                        <p>A composição da equipe é imprescindível à qualidade da prestação de serviços da rede socioassistencial. Os técnicos que acompanharão adolescentes em cumprimento de medidas em meio aberto devem integrar as categorias profissionais previstas pela Resolução CNAS nº 17/2011.</p>

