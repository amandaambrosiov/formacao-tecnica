                    <h3 class="Subtopico" id="item6">Elementos mínimos que devem constar no PIA</h3>

                    <p>Tendo como referência o Art. 54º da Lei nº 12.594/2012 constarão no Plano Individual de Atendimento, no mínimo:</p>

                    <ol>
                        <li>os resultados da avaliação interdisciplinar;</li>
                        <li>os objetivos declarados pelo adolescente;</li>
                        <li>a previsão de suas atividades de integração social e/ou capacitação profissional;</li>
                        <li>atividades de integração e apoio à família;</li>
                        <li>as formas de participação da família para efetivo cumprimento do plano individual; e</li>
                        <li>as medidas específicas de atenção à sua saúde.</li>
                    </ol>

                    <p>Para abarcar todos esses aspectos é importante que o PIA seja construído a partir de diversos conhecimentos articulados, por isso a importância da equipe multidisciplinar. O PIA é a materialização do trabalho técnico na socioeducação.</p>

                    <p>Vale destacar que o PIA para PSC tem diferenças em relação ao de LA:</p>

                    <p><i class="seta bi bi-arrow-right-short"></i> Pois inclui a participação de uma entidade que recebe o adolescente para o cumprimento da tarefa.</p>

                    <p>
                    <i class="seta bi bi-arrow-right-short"></i> Nesse caso, a unidade de atendimento do meio aberto deve reunir-se com a entidade (que já foi previamente cadastrada pelo órgão gestor) para, junto com o adolescente, construir as ações de responsabilização.
                    </p>

