                                Dessa forma, evitamos que o adolescente seja submetido à prática da reculpabilização ou mesmo de revitimização.
                            </p>
                        </div>

                        <p>Ao começar a entrevista com o adolescente ou sua família é importante explicar o que vai ser feito, o que é o PIA, deixar o adolescente falar com tranquilidade, não o interromper, respeitando sua forma de se expressar, sem críticas a gírias.</p>

                        <p><strong>A VISITA DOMICILIAR</strong></p>

                        <p>Quando a família ou o adolescente têm dificuldade em comparecer na unidade, a equipe pode avaliar fazer uma visita domiciliar, que deve ser planejada com antecedência e utilizada como último recurso.</p>

                        <p>Essas visitas não devem ser realizadas de “surpresa”, sem o consentimento dos pais ou responsáveis pelo adolescente. Visitas sem prévio aviso só podem ser feitas quando não foi possível o contato com os moradores e a demanda seja urgente.</p>

                        <div class="content_box_padrao">
                            <p>
                                Não devem ter caráter fiscalizatório ou punitivo.
                            </p>
                        </div>

                        <p>Quando a visita é realizada, é importante buscar compreender a realidade social das famílias e outras informações que possam subsidiar propostas para garantir os direitos dos usuários.</p>

                        <p>Ainda que a presença do profissional do SUAS no domicílio altere as formas de tratamento entre os membros, a visita domiciliar é uma oportunidade única de observar o adolescente e sua família sem o peso de um comportamento esperado em uma instituição, que já tem regras de convivência estabelecidas.</p>

                        <p>É fundamental conversar com o adolescente sobre como ele se sente naquele ambiente, quais elementos ali são importantes para ele, sobre o convívio com os demais parentes ou amigos que frequentam a casa e quais são as dificuldades enfrentadas para a ida à unidade.</p>

