                        <p>É preciso analisar se as metas e objetivos elencados no PIA estão sendo atingidos, se o caminho planejado está funcionando, e se os indicadores mostram que o trabalho técnico está sendo eficaz.</p>

                        <p>Quando terminamos de pactuar as metas no PIA, devemos também planejar nossa metodologia de acompanhamento. Assim, monitorar é realizar um acompanhamento sistemático do planejamento pactuado.</p>

                        <p><strong>O que é preciso para monitorar o PIA?</strong></p>

                        <p>
                            I. Estabelecer as metas e indicadores que servirão de parâmetro para a condução do PIA e sua avaliação final;<br><br>

                            II. Selecionar quais elementos usaremos: sistemas de registro e monitoramento (prontuário SUAS, prontuário SUS, SISC, RMA);<br><br>

                            III. Registrar as ações de monitoramento realizadas: o registro do monitoramento (atas de reuniões, anotações das ligações, visitas domiciliares ou institucionais, diários de campo, inclusão de dados em sistemas) é o material que mostra como o acompanhamento está sendo feito;<br><br>

                            IV. Estabelecer uma “agenda” de ações, uma programação do que será feito em dias, semanas e meses;<br><br>

                            V. Realizar “pausas” técnicas: momentos mensais de reflexão entre toda a equipe e, posteriormente, com os usuários, apresentando “balanços” do atendimento; reconhecimento público de pontos de avanço (celebrações, festas); identificação dos pontos de dificuldades e elaboração de estratégias de resolução. (BRASIL, 2022, p. 78)<br><br>
                        </p>

                        <p><strong>Especificidades de monitoramento na PSC:</strong></p>

                        <div class="content_box_padrao">
                            <p>
                                O PIA para PSC tem diferenças em relação ao de Liberdade Assistida (LA), pois inclui a participação de uma entidade que recebe o adolescente para o cumprimento da tarefa. Nesse caso, a unidade de atendimento do meio aberto deve reunir-se com a entidade (que já foi previamente cadastrada pelo órgão gestor) para, junto com o adolescente, construir as ações de responsabilização.
                            </p>
                        </div>

                        <p>Na PSC deve ser feito acompanhamento social do adolescente, da família e a entidade deve ser constantemente supervisionada. A entidade também precisa ser capacitada sobre socioeducação e a natureza da medida da PSC. (BRASIL, 2022, p. 14)</p>

