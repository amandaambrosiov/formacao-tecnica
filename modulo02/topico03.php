                        <p><strong>Como planejar a LA?</strong></p>

                        <p>Tanto a Resolução 119/2006 do CONANDA quanto a Tipificação Nacional dos Serviços Socioassistenciais de 2009 preveem atendimentos regulares, preferencialmente semanais, para abordar aspectos relacionados à construção de novas trajetórias de vida e à devida responsabilização diante das consequências do ato infracional cometido. No entanto, o acompanhamento não se limita a entrevistas semanais ou visitas aos demais serviços do SUAS.</p>

                        <p>Quando planejamos a LA, acolhemos integralmente aquele adolescente, buscando propor ações que vão ao encontro de suas demandas e que trabalhem a proteção e responsabilização. Além dos encaminhamentos básicos às políticas de saúde, educação e profissionalização, o planejamento da LA deve incluir um conjunto diversificado de atividades, que varia com cada adolescente.</p>

                        <p>As atividades da LA são diversas e não se restringem a entrevistas ou conversas semanais (ainda que elas sejam importantes).</p>

                        <p><strong>Como planejar a PSC?</strong></p>

                        <p>No âmbito do SUAS, a Prestação de Serviços à Comunidade tem as mesmas prerrogativas da LA: o acompanhamento integral do adolescente e de sua família e a promoção de seus direitos e acesso à cidadania.</p>

                        <p>No entanto, a PSC prevê que o processo de responsabilização deve ser concretizado por meio de uma atividade específica, de natureza colaborativa não laboral, cujo objetivo é contribuir com a comunidade e alterar a forma como o adolescente é enxergado pela comunidade.</p>

                        <p>Adolescentes diferentes podem ter trajetórias, sonhos e desejos similares. É recomendável aproveitar essa riqueza, combinando as potencialidades identificadas e propondo ações em dupla, trio ou grupo. Ainda que a avaliação técnica seja individual, adolescentes podem estabelecer parcerias e atuar em cooperação no cumprimento da PSC.</p>

