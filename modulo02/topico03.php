                        <h4 class="subsubtopico">2ª Etapa: Análise situacional</h4>

                        <div class="content_box_padrao_cinza">
                            <p>
                                Esse momento é crucial para a coleta de informações e a construção de um projeto junto ao usuário. Esse conjunto de dados é fundamental para a construção do projeto socioeducativo. Ele não pode ser entendido como o PIA e nem como um questionário ou um conjunto de perguntas em que o técnico preenche dados gerais, alguns subjetivos e outros relacionados a questões socioeconômicas familiares, frequência à escola e inserção profissional, dentre outros.
                            </p>
                        </div>

                        <p>A natureza das informações são quantitativas (tratam de números, escalas, proporções, sendo encontrados em cadastros, levantamentos, sistemas, pesquisas, dados epidemiológicos e questionários) e qualitativas (são atravessados por subjetividades, valores, conceitos que demandam metodologias diferenciadas para sua obtenção), ambas são importantes para a elaboração do PIA.</p>

                        <p>A organização das informações e tudo que se refere ao adolescente e sua família são fundamentais para a construção do diagnóstico socioeducativo e, consequentemente, na construção do planejamento pedagógico, onde teremos uma visão da totalidade que o cerca.</p>

                        <p>
                            É preciso construir um plano de ação, individualizado que precisará ter um norte em sua elaboração e questionamentos plausíveis para a sua construção, tais como:
                        </p>

                        <p><i class="seta bi bi-arrow-right-short"></i> O que será feito a partir das informações levantadas?</p>

                        <p><i class="seta bi bi-arrow-right-short"></i> Como será feito?</p>

                        <p><i class="seta bi bi-arrow-right-short"></i> Quem irá fazer?</p>

                        <p><i class="seta bi bi-arrow-right-short"></i> Quando deverá ser feito?</p>

                        <p><i class="seta bi bi-arrow-right-short"></i> Quais as ações para o adolescente e para a família?</p>

                        <p>A equipe técnica trará um grande volume de informações qualitativas e quantitativas que precisam ser decompostas e comparadas para que ganhem significado para o trabalho socioeducativo.</p>

                        <p>No SUAS há muitas formas de produzir e tratar as informações que formarão o diagnóstico socioeducativo, dentre elas:</p>

                        <p><i class="seta bi bi-arrow-right-short"></i> a entrevista;</p>

                        <p><i class="seta bi bi-arrow-right-short"></i> a visita domiciliar;</p>

                        <p><i class="seta bi bi-arrow-right-short"></i> Prontuário do SUAS.</p>

                        <p>O levantamento de dados gerais apenas faz sentido se formos capazes de interpretá-los e dar sentido à nossa intervenção profissional. Os valores, formas de ver e estar no mundo, pertencimento religioso, afetos e emoções que aparecem na relação com o outro e consigo mesmo são a matéria-prima do trabalho socioeducativo.</p>

                        <p>Organizar as informações e tudo o que diz respeito ao adolescente e sua família é fundamental para a construção do diagnóstico socioeducativo, que dará condições de construir o planejamento pedagógico com o adolescente, articulando sua dimensão singular com a totalidade que o cerca.</p>

                        <p>A visita domiciliar, assim como a entrevista, são recursos para se chegar a um fim desejado, não o trabalho em si.</p>

                        <p><strong>A ENTREVISTA</strong></p>

