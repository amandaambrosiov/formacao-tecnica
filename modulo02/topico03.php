                        <p><strong>Peculiaridades sobre a avaliação da medida socioeducativa de Liberdade Assistida e a de Prestação de Serviço à Comunidade</strong></p>

                        <p>Avaliando a Liberdade Assistida (LA)</p>

                        <p>De acordo com o Art. 119º do Estatuto da Criança e Adolescente, a Liberdade Assistida deve ser analisada considerando seus aspectos subjetivos e objetivos.</p>

                        <div class="content_box_padrao_cinza">
                            <p>
                                Art. 119. Incumbe ao orientador, com o apoio e a supervisão da autoridade competente, a realização dos seguintes encargos, entre outros: <br><br>

                                I. promover socialmente o adolescente e sua família, fornecendo-lhes orientação e inserindo-os, se necessário, em programa oficial ou comunitário de auxílio e assistência social;<br><br>
                                II. supervisionar a frequência e o aproveitamento escolar do adolescente, promovendo, inclusive, sua matrícula;<br><br>
                                III. diligenciar no sentido da profissionalização do adolescente e de sua inserção no mercado de trabalho;<br><br>
                                IV. apresentar relatório do caso.
                            </p>
                        </div>





                        <p>Conforme observado o artigo apresenta diversas funções que possuem aspectos que devem ser considerados e verificados seu nível de alcance os quais trataremos a seguir:</p>

                        <p>1. Quanto inciso I - Promover socialmente o adolescente e sua família, fornecendo-lhes orientação e inserindo-os, se necessário, em programa oficial ou comunitário de auxílio e assistência social é preciso considerar:
                            <br>
                            a. as vulnerabilidades identificadas;<br><br>
                            b. a inserção nas iniciativas da proteção social básica, especial ou de proteção a pessoas ameaçadas (PBF, ACESSUAS, BPC, PAIF, PAEFI, <a class="link_tooltip" data-bs-custom-class="custom-tooltip" data-bs-toggle="tooltip" data-bs-title="Programa de Proteção a Crianças e Adolescentes Ameaçados de Morte – PPCAAM, criado em 2003 e instituído inicialmente pelo Decreto 6.231/2007, substituído pelo Decreto nº 9.579, de 22 de novembro de 2018, art. 109 à 125, consiste em uma política de proteção de crianças e adolescentes ameaçados de morte, com metodologia que busca prevenir a letalidade de crianças e adolescentes em todo o Brasil, bem como de protegê-las e reinseri-las na sociedade com segurança.">PPCAM</a>, <a class="link_tooltip" data-bs-custom-class="custom-tooltip" data-bs-toggle="tooltip" data-bs-title="O PPPRO é destinado a pessoas em situação de ameaça iminente de morte; em caso de comprovada situação de risco à vida ou à integridade física que necessitam de medidas isoladas de proteção. O programa atende pessoas ameaçadas que tenham perfil para os outros programas de proteção já instituídos: Programa de Proteção a Crianças e Adolescentes Ameaçados de Morte (PPCAAM), Programa de Proteção a Vítimas e a Testemunhas Ameaçadas (Provita) e Programa Estadual de Proteção a Defensoras e Defensores dos Direitos Humanos (PEPDDH). A proteção é feita enquanto as pessoas aguardam avaliação para inclusão nos outros programas.">PPPRO</a>, PROVITA);<br><br>
                            c. como se deu esse acompanhamento e a interlocução entre as equipes de referência;<br><br>
                            d. como aconteceu a referência e a contrarreferência;<br><br>
                            e. o nível de adesão dos usuários.
                            <br><br>
                            2. Quanto ao inciso II - Supervisionar a frequência e o aproveitamento escolar do adolescente, promovendo, inclusive, sua matrícula faz-se necessário conhecer como se deu:
                            <br>
                            a. a relação com a escola ou a Secretaria de Educação;<br><br>
                            b. se houveram matrículas ou rematrículas e se houve empecilho para tal;<br><br>
                            c. se o adolescente foi partícipe nesse processo;<br><br>
                            d. se houve engajamento dele com o nivelamento idade-série;<br><br>
                            e. se houveram relatos de preconceitos ou bullying.<br><br>

                            3. Sobre o inciso III - Diligenciar no sentido da profissionalização do adolescente e de sua inserção no mercado de trabalho deve-se buscar identificar:<br>

                            a. se houve interesses da parte do adolescente no engajamento em cursos profissionalizantes e quais as dificuldades para isso;<br><br>
                            b. se foi possível fazer a inclusão em redes protegidas de trabalho para maiores de 16 anos (além da aprendizagem).<br><br>

                            4. Acerca do inciso IV - Apresentar relatório do caso deve-se atentar:<br>

                            a. se os relatórios foram feitos em tempo hábil para o Sistema de Justiça, se houve alguma dificuldade para isso;<br><br>
                            b. se a equipe pôde contar com treinamento para construção desses relatórios;<br><br>
                            c. qual o feedback do sistema de justiça para esses relatórios.
