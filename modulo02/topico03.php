                        <h4 class="subsubtopico">4ª Etapa: Monitoramento</h4>

                        <p>Não existe planejamento sem monitoramento, a não ser que entendamos o PIA como um documento guardado no fundo de uma gaveta. O monitoramento é uma rica oportunidade de transformar o planejamento em algo dinâmico e debatido;</p>

                        <p>Ele deve contemplar ações em esferas diferentes de atuação dentro do contexto socioeducativo de forma intersetorial, sendo o fio condutor que permite estabelecer o planejamento e a flexibilização na construção do PIA, sem que a equipe técnica se perca nas idas e voltas necessárias à efetivação do PIA.</p>

                        <p>O monitoramento é o exercício de ver, rever e, se preciso, reescrever a trajetória sociopedagógica. É o processo continuado que inicia na pactuação das ações do PIA.</p>

                        <div class="content_box_padrao">
                            <p>
                                Quando monitoramos o desenvolvimento do adolescente no cumprimento da medida, não buscamos vigiar seus comportamentos, disciplinar práticas ou mesmo coibir decisões feitas por ele ou por seus familiares. Esse tipo de visão de monitoramento está ultrapassado! Esse era o tom de liberdade vigiada e da doutrina da situação irregular, onde os adolescentes pobres eram tidos como “perigosos, desviantes e anormais", necessitando, sempre, de acompanhamento moral rigoroso e do controle das famílias e do Estado. (BRASIL, 2022, p. 77)
                            </p>
                        </div>

