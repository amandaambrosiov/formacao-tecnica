                        </p>

                        <p><strong>Avaliando a Prestação de Serviços à Comunidade (PSC)</strong></p>

                        <p>A avaliação na PSC compete tanto à unidade de acompanhamento do SUAS, quanto às entidades parceiras que recebem os adolescentes para a realização das atividades, de forma a ouvir os profissionais e saber quais as suas dificuldades enquanto referência neste serviço. É preciso avaliar:</p>

                        <p><i class="seta bi bi-arrow-right-short"></i>como os profissionais da entidade se sentiram;<br><br>
                            <i class="seta bi bi-arrow-right-short"></i>se houve conflitos entre eles e os adolescentes;<br><br>
                            <i class="seta bi bi-arrow-right-short"></i>se a entidade, de fato, conseguiu realizar as entregas pactuadas no PIA;<br><br>
                            <i class="seta bi bi-arrow-right-short"></i>como os adolescentes se sentiram naquela entidade.
                        </p>

                        <p>É importante que essas entidades tenham um retorno de como a PSC impactou positivamente na vida daquele adolescente após o cumprimento da medida, e quais as dificuldades enfrentadas por ele, bem como se foram sanadas.</p>

                        <p>Segundo o Caderno de Orientações Técnicas para Elaboração do Plano Individual de Atendimento (PIA) de Medidas Socioeducativas em Meio Aberto (2022, p. 87) deve-se observar na avaliação da medida de PSC:</p>

                        <p><i class="seta bi bi-arrow-right-short"></i> Atividade realizada atingiu de fato os objetivos propostos?<br><br>
                            <i class="seta bi bi-arrow-right-short"></i> O adolescente sentiu-se confortável para discutir a natureza das atividades ou teve vontade de substituí-las por outras?<br><br>
                            <i class="seta bi bi-arrow-right-short"></i> É possível perceber pelo discurso ou pelas práticas do adolescente que houve algum ganho subjetivo com a execução da atividade?<br><br>
                            <i class="seta bi bi-arrow-right-short"></i> Atividade foi importante para seu processo de responsabilização?<br><br>
                            <i class="seta bi bi-arrow-right-short"></i> O monitoramento realizado foi suficiente para identificar as dificuldades ou apenas no término da medida que foram percebidos aspectos que poderiam ter sido alterados antes?<br><br>
                            <i class="seta bi bi-arrow-right-short"></i> Houve alguma manifestação da comunidade sobre a importância da atividade realizada pelo adolescente ou mudança de visão sobre ele?
                        </p>

