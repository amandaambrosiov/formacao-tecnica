                            <div class="content_box" id="conteudo_importante">
                                <p>O art. 23º da Lei nº 8.742/1993, Lei Orgânica da Assistência Social (LOAS),- define que os serviços socioassistenciais são atividades contínuas destinadas à melhoria da vida da população, visando as necessidades básicas, e devem seguir os objetivos, princípios e diretrizes da citada norma legal. Além disso, a Política Nacional de Assistência Social determina sua organização em rede, de acordo com o nível de proteção social, aprovada pela Resolução nº 130, de 15 de julho de 2005, do CNAS (Norma Operacional Básica do Sistema Único da Assistência Social – NOB/SUAS) (BRASIL, 1993).</p>
                            </div>
                        </div>

                        <p>Muitos avanços e impactos têm sido observados a partir do novo modelo organizacional de gestão e execução de programas, projetos e serviços de assistência social. Este novo modelo dispõe sobre a oferta da proteção social básica e da proteção social especial, que visa melhorar o atendimento das expressões da questão social que atinge famílias e indivíduos.</p>

                        <p>Tendo em vista que famílias e indivíduos vivenciam e passam por diferentes estágios de vulnerabilidade e risco social, há a necessidade de destinar serviços, programas, projetos e intervenções que alcancem esses públicos, dentro de suas realidades. Algumas famílias demandam apenas apoio, orientação e acompanhamento para fortalecer seus vínculos familiares que estão fragilizados. Outras vão para além dessa necessidade, considerando que seus direitos estão ameaçados ou violados. Portanto, a Política Nacional de Assistência Social (PNAS), propõe a divisão das responsabilidades do Estado no atendimento e acompanhamento a essas famílias.</p>

                        <p>A Proteção Social Básica (PSB) atua de forma preventiva. Deve centrar-se no planejamento e implementação de contramedidas, antecipando o surgimento ou agravamento de situações de risco social e vulnerabilidades que possam dificultar o acesso dos cidadãos aos seus direitos sociais. A Proteção Social Especial (PSE) está associada às famílias que estão em situação de violações de direitos, isso significa que as demandas necessitam de atenção especializada, visando a reparação dos direitos ou mesmo a redução de danos.</p>

                        <p>A INTERLOCUÇÃO DO PAEFI E AS MEDIDAS SOCIOEDUCATIVAS EM MEIO FECHADO se diferencia da A INTERLOCUÇÃO ENTRE AS MEDIDAS EM MEIO FECHADO E EM MEIO ABERTO. A primeira se dá através do acompanhamento da família do adolescente afastado do convívio familiar devido aplicação de medida socioeducativa em meio fechado, configurando-se público-alvo do PAEFI. Já a segunda, se dá mediante progressão de medida socioeducativa, decidida pela autoridade judicial.</p>

