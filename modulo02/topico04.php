                                <p>O Serviço de Atendimento Especializado a Famílias e Indivíduos (PAEFI) é um dos serviços que compõem a Proteção Social Especial de Média Complexidade, sendo sua oferta, exclusivamente, pelo Centro de Referência Especializado de Assistência Social (CREAS).</p>
                            </div>
                        </div>

                        <p>O PAEFI apresenta como público-alvo famílias e indivíduos em situação de risco social em decorrência da violência ou violação de direitos, como: afastamento do convívio familiar devido a aplicação de medida protetiva ou socioeducativa, trabalho infantil, negligência, violência física e/ou psicológica, violência sexual, situação de rua, tráfico de pessoas, abandono, discriminação por orientação sexual e/ou raça/etnia, entre outras. O trabalho social oferece apoio, orientação e acompanhamento visando superar essas situações, promovendo direitos, mantendo e fortalecendo as relações familiares e sociais (BRASIL, 2009).</p>

