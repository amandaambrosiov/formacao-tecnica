                        <p>Para atingir seus objetivos, o PAEFI desenvolve um trabalho social composto por uma equipe multiprofissional composta por assistentes sociais, psicólogos, advogados e entre outros.</p>

                        <p>Durante o trabalho social desenvolvido junto às famílias e indivíduos, devem ser viabilizadas as atenções e orientações direcionadas para a promoção de direitos, preservação e fortalecimento de vínculos familiares, comunitários e sociais, e para o fortalecimento da função protetiva das famílias, diante do conjunto de condições que as vulnerabilizam e/ou as submetem a situações de risco pessoal e social. Com os seguintes objetivos:</p>

                        <p>
                            <i class="seta bi bi-arrow-right-short"></i>Contribuir para o fortalecimento da família no desempenho de sua função protetiva;<br><br>
                            <i class="seta bi bi-arrow-right-short"></i>Processar a inclusão das famílias no sistema de proteção social e nos serviços públicos, conforme necessidades;<br><br>
                            <i class="seta bi bi-arrow-right-short"></i>Contribuir para restaurar e preservar a integridade e as condições de autonomia dos usuários;<br><br>
                            <i class="seta bi bi-arrow-right-short"></i>Contribuir para romper com padrões violadores de direitos no interior da família;<br><br>
                            <i class="seta bi bi-arrow-right-short"></i>Contribuir para a reparação de danos e da incidência de violação de direitos;<br><br>
                            <i class="seta bi bi-arrow-right-short"></i>Prevenir a reincidência de violações de direitos.
                        </p>

