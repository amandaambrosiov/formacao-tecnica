                        <h3 class="Subtopico" id="item1">O serviço PAEFI e o acompanhamento das famílias com adolescentes em cumprimento de medida socioeducativa em meio fechado</h3>

                        <p>A Portaria Conjunta dos Ministérios da Mulher, da Família e dos Direitos Humanos e da Cidadania nº 01, de 21 de Novembro de 2022, dispõe sobre as normas gerais para a integração entre os programas de atendimento socioeducativo conforme previsto no SINASE.</p><br>

                        <hr><br>

                        <p><strong>Art. 2º</strong> Desde que observadas as exigências do Capítulo IV da Lei nº 12.594/2012 (DO PLANO INDIVIDUAL DE ATENDIMENTO), o Serviço de Proteção Social a Adolescentes em Cumprimento de Medida Socioeducativa de Liberdade Assistida (LA) e de Prestação de Serviços à Comunidade (PSC) e o programa de atendimento socioeducativo em meio aberto de competência municipal são compatíveis entre si.
                            <br><br>
                            [...]
                            <strong>§ 3º</strong> O Serviço de Proteção Social a Adolescentes em Cumprimento de Medida Socioeducativa em meio aberto, realizado no âmbito do SUAS, não se confunde com o programa de apoio e acompanhamento a egressos da medida socioeducativa do meio fechado, que deve ser ofertado exclusivamente por meio das entidades que executam as medidas de internação e semiliberdade.
                            <br><br>
                            <strong>Art. 3º</strong> Quando a autoridade judiciária determinar a substituição das medidas socioeducativas, os agentes operacionais envolvidos nos programas de atendimento devem observar os seguintes requisitos:
                            <br><br>
                            I. articulação entre as equipes dos programas de atendimento socioeducativo do meio fechado e do meio aberto envolvidas na promoção do adolescente, para o encaminhamento e a discussão do caso, de preferência por contato direto, seja por via telefônica, videoconferência ou reuniões presenciais, e especialmente pela adoção de fluxos e protocolos entre os respectivos órgãos governamentais responsáveis;
                            <br><br>
                            II. encaminhamento obrigatório do Plano Individual de Atendimento (PIA) às equipes de agentes que darão continuidade à execução da medida socioeducativa, como instrumento técnico norteador das articulações descritas no inciso I;
                            <br><br>
                            III. orientação às famílias e aos adolescentes sobre os significados, impactos e responsabilidades inerentes à nova situação; e
                            <br><br>
                            IV. acompanhamento especial dos adolescentes oriundos das medidas socioeducativas cumpridas em regime fechado [...] com a identificação e avaliação dos impactos advindos da privação de liberdade, incluindo-se a possibilidade de encaminhamento para acompanhamento terapêutico ou clínico na rede de saúde e a mobilização de programas de proteção a adolescentes ameaçados de morte.
                            <br><br>
                            <strong>Art. 4º</strong> Os cuidados em saúde iniciados durante o cumprimento de uma das medidas socioeducativas devem ser continuados, com a preservação dos planos terapêuticos singulares, independente do novo regime determinado, diante da migração do regime fechado para o aberto e vice-versa, ou do novo tipo de medida a ser cumprida, em vista da substituição da internação ou da semiliberdade por LA ou PSC e vice-versa.
                            <br><br>
                            <strong>Art. 5º</strong> A escolarização dos adolescentes em cumprimento de medidas socioeducativas deve ser mantida, cabendo à equipe responsável pelo programa de atendimento, com base nas diretrizes da Resolução nº 3/2016, da Câmara de Educação Básica do Conselho Nacional de Educação e após discussão técnica sobre cada caso com os orientadores pedagógicos das escolas, encaminhar o adolescente para a rede escolar correspondente, ou seja:

                            I. unidade de internação, quando migra do meio aberto para o regime fechado; ou<br><br>
                            II. rede regular de ensino, quando migra do regime fechado para o meio aberto.<br><br>
                            <strong>Art. 6º</strong> Os relatórios sobre o desenvolvimento dos adolescentes até o momento da substituição da medida socioeducativa, especialmente os referentes ao acompanhamento da sua saúde e da educação, devem ser encaminhados juntamente com o PIA, de que trata o inciso II do caput do art. 3º desta Portaria.
                            <br><br>
                            <strong>Art. 7º</strong> É responsabilidade do Poder Executivo municipal, em articulação com a gestão estadual do SINASE, garantir assistência às famílias dos adolescentes em cumprimento de medidas socioeducativas em regime fechado de semiliberdade ou de internação, especialmente quando forem encaminhados a outros Municípios pela eventual ausência de unidades de atendimento em seu território de origem.
                            <br><br>
                            <strong>§ 1º</strong> As famílias referidas no caput deste artigo devem estar inscritas no Cadastro Único do Governo Federal e têm direito a serem atendidas e acompanhadas pelo:
                            <br><br>
                            I. Sistema Único de Assistência Social (SUAS), no âmbito do CREAS, por meio do Serviço de Proteção e Atendimento Especializado a Famílias e Indivíduos (PAEFI), tipificado como serviço socioassistencial pela Resolução nº 109/2009, do CNAS; e<br><br>
                            II. Sistema Único de Saúde (SUS), em suas Unidades Básicas de Saúde.
                            <br><br>
                            <strong>§ 2º</strong> As equipes de agentes das unidades de semiliberdade ou de internação devem se articular com os CREAS para a:
                            <br><br>
                            I. definição de ações conjuntas, que devem, sempre que possível, compor as estratégias do Plano Individual de Atendimento; e <br><br>
                            II. institucionalização de fluxos e protocolos entre as respectivas Secretarias estaduais e municipais responsáveis.
                            [...]

                            <br><br>
                            <strong>§ 3º</strong> Para o acompanhamento pelas unidades do SUAS, a articulação de que trata o § 2º deste artigo deve ocorrer no momento inicial da admissão do adolescente para cumprimento da medida socioeducativa de semiliberdade ou de internação, competindo às equipes de agentes das unidades de regime fechado encaminharem as famílias dos adolescentes aos CREAS ou ao órgão gestor da Assistência Social para que seja ofertado o apoio e suporte social. <br>
                            <strong>§ 4º</strong> O acompanhamento das famílias pelo SUAS é complementar àquele realizado pelas equipes das unidades de atendimento das medidas socioeducativas de semiliberdade e de internação, e deve atuar exclusivamente no fortalecimento dos vínculos familiares e para garantir o retorno mais seguro dos internados aos Municípios de origem, quando for o caso.
                            <br><br>
                            <strong>Art. 8º</strong> O acompanhamento pelo SUAS consiste no trabalho social no âmbito do CREAS com as famílias dos adolescentes que estão em cumprimento de medidas socioeducativas em regime fechado, a fim de prevenir a ruptura dos vínculos familiares em virtude do afastamento do convívio familiar, o que compreende o:
                            <br><br>

                            a. apoio para o enfrentamento de todas as formas de violência, de preconceito, de discriminação e de estigmatização nas relações familiares;<br><br>
                            b. acesso a serviços socioassistenciais de Proteção Social Básica e Proteção Social Especial e indicação de acesso a benefícios sociais e programas de transferência de renda;<br><br>
                            c. acesso a serviços de outras políticas públicas setoriais, dos demais órgãos do Sistema de Garantia de Direitos, como o Sistema de Justiça; e
                            d. acesso a serviços, programas e projetos de instituições não governamentais e comunitárias.<br><br>

                            <strong>Parágrafo único.</strong> No caso de o adolescente se encontrar internado em unidades de atendimento distantes do domicílio de seus responsáveis e o Estado ainda não ofertar recursos para o deslocamento das famílias, é possível a concessão pelo Município de benefício eventual para acesso ao transporte, desde que observados os critérios do art. 22 da Lei nº 8.742, de 7 de dezembro de 1993, o Decreto nº 6.307, de 14 de dezembro de 2007, a Resolução nº 212, de 19 de outubro de 2006, do CNAS, e as Orientações Técnicas sobre Benefícios Eventuais no SUAS.
                            <br><br>
                            <strong>Art. 9º</strong> O Sistema de Informações para a Infância e Adolescência (SIPIA-SINASE), como instrumento de gestão para o monitoramento e avaliação do fluxo de atendimento socioeducativo, essencial para subsidiar ações, políticas e programas na área de adolescentes em conflito com a lei, deve ser constantemente alimentado com informações provenientes dos órgãos dos executivos estaduais e municipais responsáveis pela execução de medidas socioeducativas em regime aberto e fechado, nos termos do inciso IX do art. 4º e do inciso V do art. 5º, da Lei nº 12.594, de 18 de janeiro 2012.
                            <br><br>
                            <strong>Parágrafo único.</strong> O SUAS e o SINASE devem buscar, paulatina e permanentemente, efetivar mecanismos de interoperabilidade entre os sistemas vigentes de atendimento dos adolescentes em meio aberto e fechado.
                        </p>



