                        <h2 class="Titulo">02 - Acompanhamento ao Adolescente em Cumprimento de Medidas Socioeducativas em Meio Aberto e sua Família</h2>

                        <!-- Subsubtopico  -->
                        <h4 class="subsubtopico">A complementaridade do Serviço das Medidas Socioeducativas em meio aberto com os demais Serviços do SUAS.</h4>

                        <p>O serviço das medidas socioeducativas deve dialogar com os principais serviços da Proteção Social Básica e Especial, PAIF e PAEFI, favorecendo a qualificação do trabalho técnico, proporcionando a circulação das informações entre as equipes, assegurando o trabalho integrado dos dois serviços, de modo a garantir a proteção integral do adolescente em cumprimento de medidas socioeducativas e sua família.</p>

                        <p>Para tanto, a Resolução CNAS nº 18/2014 , reforça esse entendimento acerca da atuação de forma integrada entre os serviços do SUAS, no âmbito da execução e do acompanhamento das MSE em Meio Aberto:</p>
                        <div class="content_box_padrao_cinza">
                            <p>
                                <strong>Art. 4º</strong> A oferta do Serviço de Proteção Social a Adolescentes em Cumprimento de Medida Socioeducativa de Liberdade Assistida e Prestação de Serviços à Comunidade no âmbito do Sistema Único de Assistência Social – SUAS será complementada por meio do:
                                <br><br>
                                I. Serviço de Convivência e Fortalecimento de Vínculos-SCFV, que prioriza adolescentes em cumprimento de medidas socioeducativas, reforçando as seguranças de convívio familiar, comunitária e social e a autonomia individual, familiar e social;
                                <br><br>
                                II. Serviço de Proteção e Atendimento Especializado às Famílias e Indivíduos- PAEFI, que realiza acompanhamento familiar de forma integrada ao atendimento do adolescente pelo Serviço das Medidas Socioeducativas, estabelecendo referência e contrarreferência com o Serviço de Proteção e Atendimento Integral à Família – PAIF e atuando no contexto social de violação de direitos;
                                <br><br>
                                III. Serviço de Proteção e Atendimento Integral à Família -PAIF, que realiza o acompanhamento familiar a partir da referência e contrarreferência, com planejamento e avaliação conjunta com PAEFI e com o Serviço de Medida Socioeducativa, visando o fortalecimento do papel protetivo das famílias e atuando no contexto de vulnerabilidade e risco social e pessoal nos territórios;
                                <br><br>
                                IV. Programa Nacional de Promoção do Acesso ao Mundo do Trabalho – Acessuas Trabalho, que mobiliza, articula, encaminha e acompanha a trajetória dos adolescentes a partir de 14 anos na condição de aprendiz e a partir de 16 anos para a profissionalização, bem como de suas famílias.
                            </p>
                        </div>

                        <p>A articulação entre os serviços do SUAS deve ser garantida por meio de:</p>

                        <p><i class="seta bi bi-arrow-right-short"></i> Troca de informações;</p>
                        <p><i class="seta bi bi-arrow-right-short"></i> Definição de fluxos internos;</p>
                        <p><i class="seta bi bi-arrow-right-short"></i> Realização de reuniões entre as equipes;</p>
                        <p><i class="seta bi bi-arrow-right-short"></i> Alinhamento conceitual sobre a organização e a operacionalização dos serviços ofertados no CREAS e;</p>
                        <p><i class="seta bi bi-arrow-right-short"></i> Definição de atividades que podem ser realizadas em conjunto.</p>

